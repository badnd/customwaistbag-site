const fixEnglishPunctuation = (product) => {
  for (const key of ['title', 'tagline', 'intro']) product[key] = product[key].replaceAll(' ? ', ' — ');
  for (const item of product.specs) {
    item.value = item.label === 'Dimensions'
      ? item.value.replaceAll(' ? ', ' × ')
      : item.value.replaceAll(' ? ', ' — ');
  }
};

export function applyRussianCatalog(categories, products) {
  for (const category of categories) {
    const copy = categoryCopy[category.slug];
    category.ruTitle = copy.title;
    category.ruDescription = copy.description;
    category.description = category.description.replaceAll(' ? ', ' — ');
  }

  for (const product of products) {
    fixEnglishPunctuation(product);
    const copy = productCopy[product.model];
    product.ruType = copy.type;
    product.ruTitle = copy.title;
    product.ruTagline = copy.tagline;
    product.ruIntro = copy.intro;
    product.specs.forEach((item, index) => {
      item.ruLabel = copy.specs[index][0];
      item.ruValue = copy.specs[index][1];
    });
    product.features.forEach((item, index) => { item.ru = copy.features[index]; });
    product.colors.forEach((item, index) => { item.ru = copy.colors[index]; });
    product.logoMethods.forEach((item, index) => { item.ru = copy.logos[index]; });
    product.subjects.forEach((item, index) => { item.ru = copy.subjects[index]; });
  }
}
